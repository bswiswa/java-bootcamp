class BasicsDemo{
    static void print(){
	System.out.println("\n\nInside print...");
	System.out.println("Hello world!");
	System.out.println();
	System.out.print("Hello, world!");
	System.out.println("Hello,");
	System.out.print(" ");
	System.out.print("world!!");
    }

    public static void main(String[] args){
	print();
    }
}